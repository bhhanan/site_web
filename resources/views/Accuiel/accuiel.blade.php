Importance de l'état civil 
@endsection
@section('h2')
L’institution de l’état civil occupe une place stratégique dans l’administration marocaine pour 3 raisons : 
  d’abord c’est une mission régalienne exercée par les présidents des conseils communaux, officiers d’état civil, pour le compte de l’Etat et sous sa responsabilité ; 
 Elle a ensuite la particularité d’être l’unique institution qui accompagne le citoyen de sa naissance jusqu’à son décès ;    Enfin, modernisée, elle peut constituer, à l’aide des nouvelles technologies de l’information et de la communication, un important outil de développement fournissant 
 en temps réel aux décideurs locaux et nationaux les données démographiques nécessaires à l’action économique et sociale. Partant de cette importance, le Ministère de  l’Intérieur a toujours œuvré pour la promotion de cette institution par l’extension de son champ d’application et l’assouplissement de ses règles de façon à mettre 
  en place un état civil conforme aux standards de la société moderne.Ces efforts ont été couronnés en 2003 par la promulgation de la loi n° 37-99 qui a instauré
 un nouveau régime d’état civil qui répond aux exigences de l’actuelle société marocaine tout en respectant ses valeurs historiques, mettant ainsi fin au système dual qui , 
outre la distinction qu’il faisait entre marocains et étrangers pour le bénéfice de l’enregistrement à l’état civil, connaissait beaucoup de limites, dûes notamment 
  à l’éparpillement des textes et aux contradictions de la jurisprudence en la matière.
@endsection